    function login() {
        $email = $_POST['email'];
        $password = $_POST['password'];

        $credentials = [
            'email' => $email,
            'password' => password_hash($password, PASSWORD_BCRYPT)
        ];

        $user = User::attempt($credentials);

        if($user) {
            session_start();
            $_SESSION['logged_user_name'] = $user->name;
            $_SESSION['logged_user']= $user;

            header('Location: http://localhost:3000/day5/blog-git-demo/posts.php');

        }   
        else {
            echo "INvalid credentials<br>";
        }
    } 

