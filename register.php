    function register() {
        try {
            $name = $_POST['name'];
            $email = $_POST['email'];
            $password = $_POST['password'];

            $user = new User;
            $user->email = $email;
            $user->password = password_hash($password, PASSWORD_BCRYPT);
            $user->name = $name;

            $user->create();
        } catch(Exception $e) {            
            echo "<div class='alert alert-warning'>" . $e->getMessage(); "</div>";
        }
    } 

